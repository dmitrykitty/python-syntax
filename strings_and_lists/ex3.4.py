# Symboli specjalne

s1 = "Hello Python"
s2 = "You're the best"

print(s1, s2)  # Hello Python You're the best
print(s1 + "\n" + s2)
# Hello Python
# You're the best

print("Hello\n\tPython\n\t\tYou're the best")
# Hello
# 	Python
# 		You're the best

print("I'm the \\best\\") #I'm the \best\

print("Lubie piwo \"Zubr\"") #Lubie piwo "Zubr"
print("Lubie piwo \'Zubr\'") #Lubie piwo 'Zubr'

print(r"\tLubie \npiwo \"Zubr\"") #surowy string - ignorujemy wszystkie symbole specjalne